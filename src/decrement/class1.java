package decrement;

public class class1 {

	public static void main(String[] args) {
		class1 red = new class1();
		red.apple();
		red.orange();
		red.banana();
		red.mango();

	}
public void apple() {//print 25 to 1
for(int i = 25; i>0; i--) {
System.out.println(i);	
		
}
}
public void orange() {//print 25 to 0
for(int i = 25; i>=0; i--) {
System.out.println(i);	
		
}
}	
public void banana() {//print 50 to 0
for(int i = 50; i>=0; i--) {
System.out.println(i);	
		
}
}	
public void mango() {//print 50 to 2
for(int i = 50; i>1; i--) {
System.out.println(i);	
		
}
}	
}
